(function () {
    'use strict';

    angular
        .module('app')
        .factory('houseService', houseService);

    houseService.$inject = ['$http'];

    function houseService($http) {
        var service = {
            createHouse: createHouse,
            createRoom: createRoom,
            deleteHouse: deleteHouse,
            deleteRoom: deleteRoom,
            getHouses: getHouses,
            getRooms: getRooms
        };

        return service;

        function createHouse(house) {
            return $http({method: 'POST', url: '/House/create', data: house}).
                success(function(data, status, headers, config) {
                    // this callback will be called asynchronously
                    // when the response is available
                    return data;
                }).
                error(function(data, status, headers, config) {
                    // called asynchronously if an error occurs
                    // or server returns response with an error status.
                    
                });
        }

        function createRoom(room) {
            return $http({method: 'POST', url: '/Room/create',data: room }).
                success(function(data, status, headers, config) {
                    // this callback will be called asynchronously
                    // when the response is available
                    return data;
                }).
                error(function(data, status, headers, config) {
                    // called asynchronously if an error occurs
                    // or server returns response with an error status.
                });
        }

        function deleteHouse(id) {
            return $http({method: 'POST', url: '/House/destroy', data: { id: id } }).
                success(function(data, status, headers, config) {
                    // this callback will be called asynchronously
                    // when the response is available
                    return data;
                }).
                error(function(data, status, headers, config) {
                    // called asynchronously if an error occurs
                    // or server returns response with an error status.
                });
        }

        function deleteRoom(id) {
            return $http({method: 'POST', url: '/Room/destroy', data: { id: id } }).
                success(function(data, status, headers, config) {
                    // this callback will be called asynchronously
                    // when the response is available
                    return data;
                }).
                error(function(data, status, headers, config) {
                    // called asynchronously if an error occurs
                    // or server returns response with an error status.
                });
        }

        function getHouses() {
            return $http({method: 'POST', url: '/House/index'}).
                success(function(data, status, headers, config) {
                    // this callback will be called asynchronously
                    // when the response is available
                    return data;
                }).
                error(function(data, status, headers, config) {
                    // called asynchronously if an error occurs
                    // or server returns response with an error status.
                });
        }

        function getRooms() {
            return $http({method: 'POST', url: '/Room/index'}).
                success(function(data, status, headers, config) {
                    // this callback will be called asynchronously
                    // when the response is available
                    return data;
                }).
                error(function(data, status, headers, config) {
                    // called asynchronously if an error occurs
                    // or server returns response with an error status.
                });
        }
    }
})();